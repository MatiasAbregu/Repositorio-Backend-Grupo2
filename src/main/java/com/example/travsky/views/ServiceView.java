package com.example.travsky.views;

/**
 * Clase que define diferentes vistas del objeto Service para organizar la información acorde el controlador lo pida.
 */
public class ServiceView {
    
    /**
     * Clase estática que representa la vista simple de la entidad Service.
     * Contiene solo la información básica del servicio y muestra todo menos destino y fecha.
     */
    public static class SimpleService {}
    
    /**
     * Clase estática que representa la vista detallada de la entidad Service.
     * Contiene toda la información del servicio.
     */
    public static class DetailedService {}
}
